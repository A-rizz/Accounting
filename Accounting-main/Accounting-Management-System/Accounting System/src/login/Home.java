    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Connection con = null;
        PreparedStatement pst = null;
        ResultSet rs = null;

        try {
            // Establish connection to the database
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/accounting", "root", "");

            // Get table model
            DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();

            // SQL queries
            String checkSql = "SELECT amount FROM clientsrecord WHERE clientId = ? AND clientName = ?";
            String updateSql = "UPDATE clientsrecord SET amount = ?, date = ? WHERE clientId = ? AND clientName = ?";
            String insertSql = "INSERT INTO clientsrecord (clientId, clientName, date, amount) VALUES (?, ?, ?, ?)";

            // Iterate over table rows
            for (int i = 0; i < dt.getRowCount(); i++) {
                String clientIdStr = dt.getValueAt(i, 0).toString();
                String clientName = dt.getValueAt(i, 1).toString();
                int clientId = Integer.parseInt(clientIdStr);
                String amountStr = dt.getValueAt(i, 3).toString();
                double amount = Double.parseDouble(amountStr);
                String date = dt.getValueAt(i, 2).toString();

                // Check if the clientId and clientName already exist
                pst = con.prepareStatement(checkSql);
                pst.setInt(1, clientId);
                pst.setString(2, clientName);
                rs = pst.executeQuery();

                if (rs.next()) {
                    // If the record exists, update the amount and date
                    double existingAmount = rs.getDouble("amount");
                    double newAmount = existingAmount + amount;

                    pst = con.prepareStatement(updateSql);
                    pst.setDouble(1, newAmount);
                    pst.setString(2, date);
                    pst.setInt(3, clientId);
                    pst.setString(4, clientName);
                    pst.executeUpdate();
                } else {
                    // If the record does not exist, insert a new one
                    pst = con.prepareStatement(insertSql);
                    pst.setInt(1, clientId);
                    pst.setString(2, clientName);
                    pst.setString(3, date);
                    pst.setDouble(4, amount);
                    pst.executeUpdate();
                }
            }

            // Clear all rows from the table after saving
            dt.setRowCount(0);

            // Show success message
            javax.swing.JOptionPane.showMessageDialog(this, "Data processed successfully!");

        } catch (SQLException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
                if (pst != null) {
                    pst.close();
                }
                if (con != null) {
                    con.close();
                }
            } catch (SQLException ex) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error closing resources: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

